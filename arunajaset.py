myset = {"apple", "banana", "cherry"}
print(myset)
print(myset.add("orange"))
print(myset.copy())
y = {"google", "microsoft", "apple"}
print(myset.difference(y))
print(myset.discard("apple"))
print(myset.remove("banana"))
print(myset.union(y))
print(myset.isdisjoint(y))
print(myset.clear())
